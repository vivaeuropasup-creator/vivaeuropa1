#!/bin/bash

echo "=== ПОЛНАЯ ПРОВЕРКА ТЕКСТА НА ОШИБКИ ==="
echo ""

# 1. Ищем конкретную ошибку "Получение готового ВНЖ и адаптацией"
echo "1. НЕПРАВИЛЬНАЯ ФРАЗА 'Получение готового ВНЖ и адаптацией':"
echo "------------------------"
grep -rn "Получение готового ВНЖ и адаптацией\|получение готового ВНЖ и адаптацией" *.html

# 2. Ищем другие неправильные конструкции с творительным падежом
echo ""
echo "2. НЕПРАВИЛЬНЫЕ ПАДЕЖИ:"
echo "------------------------"
grep -rn "и адаптацией\|и другими вопросами адаптации" *.html

# 3. Ищем незаконченные предложения
echo ""
echo "3. НЕЗАКОНЧЕННЫЕ ПРЕДЛОЖЕНИЯ:"
echo "------------------------"
grep -rn "\. \.\|, ,\|и \.$\|и\.$" *.html

# 4. Ищем неправильные конструкции типа "Получаем готовый ВНЖ, и другими"
echo ""
echo "4. НЕПРАВИЛЬНЫЕ КОНСТРУКЦИИ:"
echo "------------------------"
grep -rn "Получаем.*и другими\|получаем.*и другими" *.html

# 5. Ищем проблемы с согласованием
echo ""
echo "5. ПРОБЛЕМЫ СОГЛАСОВАНИЯ:"
echo "------------------------"
grep -rn "готовый ВНЖ.*адаптацией\|готового ВНЖ.*адаптацией" *.html

# 6. Ищем неправильное использование падежей после предлогов
echo ""
echo "6. ПАДЕЖИ ПОСЛЕ ПРЕДЛОГОВ:"
echo "------------------------"
grep -rn "помощь в.*адаптацией\|помощь с.*адаптацией" *.html

echo ""
echo "=== КОНЕЦ ПРОВЕРКИ ==="
